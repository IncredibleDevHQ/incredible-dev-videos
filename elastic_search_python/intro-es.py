# Elastic Search 
# - is a Analytics and full text search engine

# What is it used for?
# > APM (Application Program Management)
# > Send events (logs,clicks ...etc)
# > Forcasting , Machine Learning , Anomoly Detection 

# Download the docker image by running - ` docker pull docker.elastic.co/elasticsearch/elasticsearch:7.11.2`
# Start a single node cluster with - ` docker run -p 9200:9200 -p 9300:9300 -e "discovery.type=single-node" docker.elastic.co/elasticsearch/elasticsearch:7.11.2 `
